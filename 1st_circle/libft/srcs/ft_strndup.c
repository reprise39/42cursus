#include "libft.h"

char	*ft_strndup(const char *str, size_t len)
{
	char	*sub;
	size_t	i;

	if(str == NULL)
		return NULL;
	sub = malloc(sizeof(char) * (len + 1));
	if (!sub)
		return (NULL);
	i = 0;
	while (i < len)
	{
		sub[i] = str[i];
		i++;
	}
	sub[i] = '\0';
	return (sub);
}
